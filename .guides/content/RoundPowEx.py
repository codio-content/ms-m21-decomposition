#Examples of round and pow

num = 3.56745623 * 22
print(num)
print()
print ('round to 2 places is',round(num,2))
print()
print ('round to 3 places is',round(num,3))
print()
print ('round to 4 places is',round(num,4))
print()
print ('round to 7 places is',round(num,7))
print()
print('4 squared is', pow(4,2))
print()
print('7 cubed is', pow(7,3))
print()
print('5 to the 4th power is', pow(5,4))
